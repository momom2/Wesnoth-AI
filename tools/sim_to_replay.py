def _wml_for_command(rc: RecordedCommand,
                     next_side: Optional[int] = None) -> str:
    """Render one RecordedCommand as a `[command]...[/command]` WML
    block. Coordinates are converted from the sim's 0-indexed
    convention to Wesnoth's 1-indexed WML by adding 1.

    `next_side` is required for end_turn (the side whose init_side
    comes next); it's derived from the history by `_build_replay_wml`.
    """
    if rc.kind == "init_side":
        side_n = rc.cmd[1]
        return (
            "\t[command]\n"
            "\t\t[init_side]\n"
            f"\t\t\tside_number={side_n}\n"
            "\t\t[/init_side]\n"
            "\t[/command]\n"
        )

    if rc.kind == "end_turn":
        # CRITICAL: emit `next_player_number = side + 1`, NOT a
        # modulo'd value. Wesnoth's `skip_empty_sides` decides
        # "is this a new turn?" by checking whether the requested
        # side index changes after modulo wrap-around. If we
        # already wrap (e.g., emit "1" after side 2 in 2p), Wesnoth
        # sees `1 == 1` post-modulo and concludes it's the SAME
        # turn -- so `next_turn()` never fires, `turn_` stays at 1
        # forever, and per-side MP never resets on init_side
        # (the `if (turn() > 1)` check in do_init_side fails).
        # That makes turn 2's first leader move corrupt because
        # the unit still has its leftover MP from turn 1.
        # Real replays emit "3" after side 2's end_turn in 2p; we
        # mirror that pattern here. Caller can override via
        # `next_side` if it has explicit history info, but the
        # default (side+1) is what Wesnoth expects.
        nxt = next_side if next_side is not None else rc.side + 1
        return (
            "\t[command]\n"
            "\t\t[end_turn]\n"
            f"\t\t\tnext_player_number={nxt}\n"
            "\t\t[/end_turn]\n"
            "\t[/command]\n"
        )

    if rc.kind == "move":
        # cmd = ["move", [sx, tx], [sy, ty], from_side]
        xs, ys = rc.cmd[1], rc.cmd[2]
        # Convert to 1-indexed.
        xs1 = ",".join(str(v + 1) for v in xs)
        ys1 = ",".join(str(v + 1) for v in ys)
        return (
            "\t[command]\n"
            f"\t\tfrom_side={rc.side}\n"
            "\t\t[move]\n"
            f"\t\t\tx=\"{xs1}\"\n"
            f"\t\t\ty=\"{ys1}\"\n"
            "\t\t[/move]\n"
            "\t[/command]\n"
        )

    if rc.kind == "attack":
        # cmd = ["attack", ax, ay, dx, dy, a_weapon, d_weapon, seed_hex]
        ax, ay, dx, dy = rc.cmd[1], rc.cmd[2], rc.cmd[3], rc.cmd[4]
        weapon = rc.cmd[5]
        d_weapon = rc.cmd[6] if len(rc.cmd) > 6 else -1
        return (
            "\t[command]\n"
            f"\t\tfrom_side={rc.side}\n"
            "\t\t[attack]\n"
            f"\t\t\tweapon={weapon}\n"
            f"\t\t\tdefender_weapon={d_weapon}\n"
            "\t\t\t[source]\n"
            f"\t\t\t\tx={ax + 1}\n"
            f"\t\t\t\ty={ay + 1}\n"
            "\t\t\t[/source]\n"
            "\t\t\t[destination]\n"
            f"\t\t\t\tx={dx + 1}\n"
            f"\t\t\t\ty={dy + 1}\n"
            "\t\t\t[/destination]\n"
            "\t\t[/attack]\n"
            "\t[/command]\n"
        )

    if rc.kind == "recruit":
        # cmd = ["recruit", unit_type, tx, ty, trait_seed]
        unit_type = _wml_quote(str(rc.cmd[1]))
        tx, ty = rc.cmd[2], rc.cmd[3]
        leader_pos = rc.extras.get("leader_pos", (tx, ty))
        lx, ly = leader_pos
        return (
            "\t[command]\n"
            f"\t\tfrom_side={rc.side}\n"
            "\t\t[recruit]\n"
            f"\t\t\ttype=\"{unit_type}\"\n"
            f"\t\t\tx={tx + 1}\n"
            f"\t\t\ty={ty + 1}\n"
            "\t\t\t[from]\n"
            f"\t\t\t\tx={lx + 1}\n"
            f"\t\t\t\ty={ly + 1}\n"
            "\t\t\t[/from]\n"
            "\t\t[/recruit]\n"
            "\t[/command]\n"
        )

    if rc.kind == "recall":
        # cmd = ["recall", unit_id, tx, ty]
        unit_id = _wml_quote(str(rc.cmd[1]))
        tx, ty = rc.cmd[2], rc.cmd[3]
        leader_pos = rc.extras.get("leader_pos", (tx, ty))
        lx, ly = leader_pos
        return (
            "\t[command]\n"
            f"\t\tfrom_side={rc.side}\n"
            "\t\t[recall]\n"
            f"\t\t\tvalue=\"{unit_id}\"\n"
            f"\t\t\tx={tx + 1}\n"
            f"\t\t\ty={ty + 1}\n"
            "\t\t\t[from]\n"
            f"\t\t\t\tx={lx + 1}\n"
            f"\t\t\t\ty={ly + 1}\n"
            "\t\t\t[/from]\n"
            "\t\t[/recall]\n"
            "\t[/command]\n"
        )

    log.warning(f"sim_to_replay: skipping unknown command kind: {rc.kind!r}")
    return ""


def _seed_from_recorded(rc: RecordedCommand) -> str:
    """Pull the seed the sim already used for this command. We assume
    the sim populated it via wesnoth_sim.request_seed(N) -- making
    the replay's [random_seed] match what the sim used internally,
    so trait/damage rolls agree bit-exact between sim and Wesnoth."""
    if rc.kind == "recruit":
        # cmd = ["recruit", unit_type, tx, ty, seed]
        return rc.cmd[4] if len(rc.cmd) > 4 and rc.cmd[4] else "00000000"
    if rc.kind == "attack":
        # cmd = ["attack", ax, ay, dx, dy, a_w, d_w, seed]
        return rc.cmd[7] if len(rc.cmd) > 7 and rc.cmd[7] else "00000000"
    return "00000000"


# ---------------------------------------------------------------------
# RNG-consumption gating
# ---------------------------------------------------------------------
# Wesnoth's synced_rng is LAZY: the seed from `[random_seed]` is only
# fetched on the FIRST `get_next_random()` call inside a synced
# command (see wesnoth_src/src/random_synced.cpp:31-49). If a recruit
# never calls RNG -- e.g. Skeleton (musthave=['undead'], num_traits=1,
# pool_size=0, single-gender) -- the seed is never fetched. Our
# `[random_seed]` follow-up is then orphaned: the outer replay loop
# sees a `dependent="yes"` command while is_unsynced=true and fires
# "found dependent command in replay while is_synced=false".
#
# So we have to predict whether each synced command consumes RNG and
# only emit `[random_seed]` for those that do. Real Wesnoth replays
# do this implicitly because the seed-write only happens when the
# server actually generated a seed.

_RNG_INFO_CACHE: Dict[str, dict] = {}


def _unit_trait_info(unit_type: str) -> dict:
    """Return the trait/gender info we need to predict synced RNG
    consumption for a recruit. Returns {} on lookup failure."""
    if unit_type in _RNG_INFO_CACHE:
        return _RNG_INFO_CACHE[unit_type]
    try:
        import json
        path = Path(__file__).resolve().parent.parent / "unit_stats.json"
        with path.open(encoding="utf-8") as f:
            data = json.load(f)
        u = data.get("units", {}).get(unit_type, {})
        t = u.get("traits", {}) or {}
        info = {
            "n_genders":  int(u.get("n_genders", 1)),
            "num_traits": int(t.get("num_traits", 2)),
            "musthave":   list(t.get("musthave", [])),
            "pool":       list(t.get("pool", [])),
        }
        _RNG_INFO_CACHE[unit_type] = info
        return info
    except Exception:
        _RNG_INFO_CACHE[unit_type] = {}
        return {}


def _recruit_consumes_synced_rng(unit_type: str) -> bool:
    """True iff Wesnoth's recruit handler calls `get_random_int` for
    this unit type, which is the trigger for `synced_rng::initialize`
    fetching the [random_seed] payload.

    Wesnoth's order in `unit::init` (wesnoth_src/src/units/unit.cpp:714):
      1. generate_gender -- one synced RNG call iff genders.size() > 1
         (single-gender unit_types like Skeleton short-circuit and
         consume zero).
      2. facing_ -- uses default_instance() (NON-synced); doesn't
         touch our seed.
      3. advance_to -> generate_traits -- per missing trait beyond
         musthaves, one synced call to pick from the available pool.
         If musthaves already fill num_traits OR the pool is empty
         after filtering, the for-loop never enters.
    """
    info = _unit_trait_info(unit_type)
    if not info:
        # Unknown unit type -- be conservative and emit the seed.
        # Worst case Wesnoth ignores it; missing seed produces the
        # actual OOS we're trying to fix.
        return True
    if info["n_genders"] > 1:
        return True
    n_random = max(0, info["num_traits"] - len(info["musthave"]))
    if n_random <= 0:
        return False
    # If pool minus already-applied musthaves is empty, the loop
    # exits via the `if(candidate_traits.empty()) break` branch
    # without calling RNG.
    available = [t for t in info["pool"] if t not in info["musthave"]]
    return len(available) > 0


def _command_consumes_synced_rng(rc: RecordedCommand) -> bool:
    """Whether a recorded command's Wesnoth-side execution calls
    synced RNG (and thus should receive a [random_seed] follow-up).
    Recruit: depends on unit type (see _recruit_consumes_synced_rng).
    Attack: always (combat rolls every strike's hit/miss).
    Other kinds: never (caller doesn't reach here)."""
    if rc.kind == "recruit":
        unit_type = rc.cmd[1] if len(rc.cmd) > 1 else ""
        return _recruit_consumes_synced_rng(unit_type)
    if rc.kind == "attack":
        return True
    return False


def _wml_choose_command(side: int, value: int) -> str:
    """Emit a synthetic `[command] dependent="yes" [choose]` block --
    Wesnoth's record of an advancement choice. After every attack that
    levels up the attacker and/or defender, `attack_unit_and_advance`
    (wesnoth_src/src/actions/attack.cpp:1556-1573) calls
    `advance_unit_at` per advancing unit, which goes through
    `mp_sync::get_user_choice("choose", ...)`. The replay records the
    chosen advancement index here. Without these, replay playback
    errors with "expecting a user choice" the first time a unit levels
    up mid-battle.

    Order in the WML stream MUST be: attack [command], its
    [random_seed] follow-up, then attacker's [choose] (if attacker
    advanced), then defender's [choose] (if defender advanced).

    Format mirrors a real-replay sample (`from_side` is the unit's
    OWNER side, not "server" -- different from [random_seed] which is
    server-emitted)."""
    return (
        "[command]\n"
        "dependent=\"yes\"\n"
        f"from_side=\"{side}\"\n"
        "[choose]\n"
        f"value=\"{value}\"\n"
        "[/choose]\n"
        "[/command]\n"
    )


def _wml_random_seed_command(seed: str, request_id: int) -> str:
    """Emit a synthetic `[command] ... [random_seed]` block. Wesnoth's
    replay engine demands one of these after every command that needs
    a random roll (recruit -> trait roll, attack -> damage rolls);
    without them you get 'expecting a user choice' on playback. The
    seed must be the SAME hex value the sim used internally so both
    sides agree on traits / damage.

    Indentation: emitted unindented to match the formatting Wesnoth
    itself produces for dependent commands. WML's parser is supposed
    to be whitespace-insensitive, but matching the real-replay style
    is one less variable when debugging OOS issues."""
    return (
        "[command]\n"
        "dependent=\"yes\"\n"
        "from_side=\"server\"\n"
        "[random_seed]\n"
        f"new_seed=\"{seed}\"\n"
        f"request_id=\"{request_id}\"\n"
        "[/random_seed]\n"
        "[/command]\n"
    )


# Commands that need a [random_seed] follow-up. Verified for recruit
# (trait roll) and attack (damage roll) by inspecting real Wesnoth
# replays. Recall does NOT need one -- recalled units' traits were
# already rolled at recruit time.
_NEEDS_RNG = frozenset({"recruit", "attack"})


def _build_replay_wml(history: List[RecordedCommand]) -> str:
    """Emit `[replay] ... [/replay]` content from a command history.
    After each random-rolling command we emit a synthetic
    `[random_seed]` follow-up so Wesnoth's replay engine has a 'user
    choice' to consume.

    end_turn's `next_player_number` is left unset here so the emitter
    falls back to `rc.side + 1` -- the convention Wesnoth's
    skip_empty_sides expects for detecting turn wrap-around. (Looking
    up the actual next init_side and using that value would emit "1"
    after side 2 in 2p, which Wesnoth treats as 'still on turn 1' --
    breaking turn 2's MP reset.)
    """
    parts: List[str] = []
    request_id = 0
    for rc in history:
        parts.append(_wml_for_command(rc, next_side=None))
        # Only emit [random_seed] for commands Wesnoth's synced_rng
        # will actually consume. Skeleton-class recruits with no
        # random traits never call get_next_random; emitting a
        # dangling [random_seed] for them produces "found dependent
        # command in replay while is_synced=false" on the NEXT
        # outer-loop iteration.
        if rc.kind in _NEEDS_RNG and _command_consumes_synced_rng(rc):
            request_id += 1
            seed = _seed_from_recorded(rc)
            parts.append(_wml_random_seed_command(seed, request_id))
        # Advancement [choose] follow-ups: emitted AFTER the attack's
        # [random_seed] so the order matches Wesnoth's
        # `attack_unit_and_advance` path (attack -> RNG follow-up ->
        # advancer per side, attacker first then defender). Sim's
        # `_maybe_advance_unit` always picks targets[0], so value=0.
        if rc.kind == "attack":
            for side, idx in rc.extras.get("advance_choices", []):
                parts.append(_wml_choose_command(side, idx))
    return "[replay]\n" + "".join(parts) + "[/replay]\n"


