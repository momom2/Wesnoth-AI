"""Export a simulated game as a Wesnoth-loadable replay (.bz2).

Why: lets the user inspect simulated games directly in Wesnoth's GUI
to validate that the simulator's behavior matches Wesnoth's. Open the
exported file via Wesnoth's main menu -> Load Game -> Replays.

Strategy: a replay file is bz2-compressed WML. After
`[/scenario]` Wesnoth has a `[multiplayer]` block, optional
`[modification]`s, then a final `[replay]` block holding the command
stream. We take a SOURCE replay's bz2 (used as the scenario template,
since the simulator's initial state was built from it), strip the
final `[replay]`, and append our own command stream from the
simulator's `command_history`.

Caveats / known compromises:
- We don't emit `[checkup]` blocks (Wesnoth uses these to detect OOS
  on rejoin; absent on local replay playback Wesnoth will compute
  results from scratch).
- We DO emit `[random_seed]` dependent commands after each recruit /
  attack -- Wesnoth's replay engine requires them ("found [recruit]
  command in replay expecting a user choice" if missing). The seed
  itself is just an 8-hex-char value generated by the exporter; on
  playback Wesnoth uses it to roll traits / damage. Different seeds
  than the sim used internally, so per-strike hit/miss / trait rolls
  on playback will differ from the sim. Unit positions, turn
  structure, and which actions happened remain correct.
- Recruit/recall must include a `[from]` block with the leader's
  keep position; the sim records this at recruit time so the
  exporter doesn't have to reconstruct game state.
- We assume coords in `command_history` are 0-indexed (the sim's
  internal convention -- see replay_dataset.parse_map_data); the
  exporter adds 1 when emitting WML.

CLI usage:
    python tools/sim_to_replay.py SOURCE.bz2 OUT.bz2
    -- self-play one game from SOURCE's initial state (DummyPolicy)
       and write the export to OUT.

Library usage:
    from wesnoth_sim import WesnothSim
    from sim_to_replay import export_replay
    sim = WesnothSim.from_replay("foo.json.gz")
    sim.run_game(p1, p2)
    export_replay(sim, source_bz2="replays_raw/.../foo.bz2",
                  out_path="my_sim.bz2")
"""

