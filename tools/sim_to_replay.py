# Source-file splicing
# ---------------------------------------------------------------------

def _find_final_replay_block(text: str) -> Optional[tuple]:
    """Locate the offsets of the final `[replay]...[/replay]` block in
    a decompressed replay file. Returns (start, end) where start is
    the offset of the `[replay]` line and end is just past `[/replay]`.

    The file may have an empty initial `[replay]` block (with just
    `[upload_log]`) before `[scenario]`; we always want the LAST one
    in the file, after `[/scenario]`."""
    # Find every [replay] / [/replay] pair, take the last one.
    last_open = text.rfind("[replay]")
    if last_open < 0:
        return None
    last_close = text.find("[/replay]", last_open)
    if last_close < 0:
        return None
    return (last_open, last_close + len("[/replay]"))


def _rewrite_pvp_settings(text: str, defaults: PvPDefaults) -> str:
    """Rewrite the source bz2's [scenario] / [side] / [multiplayer]
    blocks so Wesnoth's playback uses the same PvP settings the sim
    used for its decisions. If we don't do this, the sim's view of
    income / starting gold / experience differs from Wesnoth's view,
    and the policy's recruit decisions (gated on gold) emit moves
    that Wesnoth's playback then rejects (insufficient gold) -- OOS.

    Updates in place via regex (whitespace-insensitive WML doesn't
    care about exact formatting):
      - top-level `mp_use_map_settings="yes"` (force "use map
        settings" semantics so [scenario]'s embedded values dominate
        any host-customized mp_* values).
      - top-level `mp_village_gold`, `mp_village_support`.
      - [scenario] `experience_modifier`.
      - each [side] gold / income (income is a side-level offset
        added to game_config::base_income=2; we set income=0 so the
        effective base income equals the default 2).
      - each [side] village_gold, village_support (per-side overrides
        that some replays set).
    """
    def repl_attr(name: str, value, content: str, flags=re.MULTILINE) -> str:
        # Replace `name=...` line with `name="value"`. If absent, leave
        # alone (Wesnoth will use its own default which matches our
        # defaults for the standard ladder cases).
        return re.sub(
            rf'^(\s*){re.escape(name)}=\"?[^\"\n]*\"?',
            rf'\1{name}="{value}"',
            content,
            flags=flags,
        )

    text = repl_attr("mp_use_map_settings", "yes", text)
    text = repl_attr("mp_village_gold",     defaults.village_gold, text)
    text = repl_attr("mp_village_support",  defaults.village_support, text)
    text = repl_attr("experience_modifier", defaults.experience_modifier, text)

    # Per-side rewrite: walk each [side]...[/side] block and rewrite
    # its gold / income / village_gold / village_support attrs.
    def rewrite_side(m: "re.Match") -> str:
        block = m.group(0)
        block = repl_attr("gold",            defaults.starting_gold, block)
        block = repl_attr("income",          0, block)  # offset; effective = 2
        block = repl_attr("village_gold",    defaults.village_gold, block)
        block = repl_attr("village_support", defaults.village_support, block)
        return block
    text = re.sub(
        r'\[side\][\s\S]*?\[/side\]',
        rewrite_side,
        text,
    )
    return text


def export_replay(
    sim:        WesnothSim,
    source_bz2: Path,
    out_path:   Path,
    *,
    pvp_defaults: Optional[PvPDefaults] = None,
) -> None:
    """Write `out_path` (a .bz2) such that Wesnoth can load it as a
    replay reproducing `sim`'s game from its initial state.

    `source_bz2` MUST be the same replay the sim was initialized from
    (via `WesnothSim.from_replay(<json.gz>)`). The .bz2 carries the
    full scenario WML the sim's initial state was derived from --
    map, sides, factions, leader stats. We can't reconstruct that
    from the sim's GameState alone (we'd lose modifications, era
    config, ai settings, mp_era_name, etc.).

    `pvp_defaults`: when provided, rewrite the source's
    economy/experience settings to match self-play "use map settings"
    behavior. Pass the SAME `PvPDefaults` you passed to
    `WesnothSim.from_replay` so sim and Wesnoth playback agree.
    """
    source_bz2 = Path(source_bz2)
    out_path = Path(out_path)
    log.info(f"loading source replay {source_bz2}")
    with bz2.open(source_bz2, "rt", encoding="utf-8", errors="ignore") as f:
        text = f.read()

    span = _find_final_replay_block(text)
    if span is None:
        raise RuntimeError(
            f"{source_bz2}: could not locate final [replay] block. "
            "File doesn't look like a Wesnoth replay.")
    start, end = span

    # Force oos_debug="no" in the top-level header. When the source
    # replay was saved with oos_debug="yes" (the host had "Strict
    # synchronization" enabled in the lobby), Wesnoth replaces every
    # synced command's checkup with `mp_debug_checkup`, which calls
    # `get_user_choice` against our (non-existent) `[checkup]` blocks
    # and immediately fires "expecting a user choice" on the very
    # first command -- even on a hand-built minimal trace with no
    # recruits or moves. Our exports don't emit `[checkup]` blocks,
    # so they're incompatible with debug-mode strictness; downgrading
    # to standard mode is safe and lossless for our use case.
    text = re.sub(
        r'^oos_debug=\"yes\"',
        'oos_debug="no"',
        text,
        count=1,
        flags=re.MULTILINE,
    )
    if pvp_defaults is not None:
        text = _rewrite_pvp_settings(text, pvp_defaults)
    span = _find_final_replay_block(text)  # re-locate after edits
    start, end = span

    new_replay = _build_replay_wml(sim.command_history)
    spliced = text[:start] + new_replay + text[end:]

    log.info(f"writing {out_path} ({len(sim.command_history)} commands)")
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with bz2.open(out_path, "wt", encoding="utf-8") as f:
        f.write(spliced)


# ---------------------------------------------------------------------
# Source mapping (json.gz <-> .bz2)
# ---------------------------------------------------------------------

def find_source_bz2(json_gz_path: Path,
                    replays_raw: Path = Path("replays_raw")) -> Optional[Path]:
    """Given the path of a `replays_dataset/<id>.json.gz`, find the
    corresponding `replays_raw/<date>/<game_id>.bz2`. Returns None
    if not found (e.g., the user reorganized replays_raw)."""
    import gzip, json
    with gzip.open(json_gz_path, "rt", encoding="utf-8") as f:
        data = json.load(f)
    game_id = data.get("game_id", "")
    if not game_id:
        return None
    # game_id has double-underscore separating prefix from rest, but
    # the .bz2 filename uses the same convention. Try the literal
    # game_id first (matches recent dumps), then the legacy `_-_`
    # variant.
    candidates = [game_id + ".bz2",
                  game_id.replace("__", "_-_", 1) + ".bz2"]
    replays_raw = Path(replays_raw)
    if not replays_raw.exists():
        return None
    for date_dir in sorted(replays_raw.iterdir()):
        if not date_dir.is_dir():
            continue
        for cand in candidates:
            p = date_dir / cand
            if p.exists():
                return p
    return None


